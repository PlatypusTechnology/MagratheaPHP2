<h2>This documentation site</h2>
<p>Method and class signatures on this site are generated live via PHP Reflection over the
framework's own source — never hand-copied — so they can't silently go stale. The narrative
guides are hand-curated and flagged when the underlying source has changed since they were
last written.</p>
<p>Built by <strong>Claude</strong> (Anthropic), working from Paulo's direction, as a
from-scratch replacement for the previous phpDocumentor-generated site.</p>

